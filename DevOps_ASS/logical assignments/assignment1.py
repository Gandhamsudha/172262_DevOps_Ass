

  
# abs() built-in function 
  
# floating point number 
float = -54.26
print('Absolute value of float -54.26 is:', abs(float)) 
  
# An integer 
int = -94
print('Absolute value of integer -94 is:', abs(int)) 
  
# A complex number 
complex = (3 - 4j) 
print('Absolute value or Magnitude of 3-4j complex is:', abs(complex)) 